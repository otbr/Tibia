void Map::LoadMap(int arr[40][60])
{


	for (int row = 0; row < 40; row++)
	{
		for (int column = 0; column < 60; column++)
			map[row][column] = arr[row][column];
	}
}

void Map::DrawMap(Game* points)
{
	int type = 0;
	SDL_RenderSetViewport(Game::renderer, &destB);
	TextureManager::Draw(backgroundTex, srcB, destB);
	
	SDL_Rect viewingPort;

	viewingPort.h = 800;
	viewingPort.w = 1150;
	viewingPort.x = 200;
	viewingPort.y = 63;

	SDL_RenderSetViewport(Game::renderer, &viewingPort);


	for (int row = 0; row < 40; row++)
	{
		for (int column = 0; column < 60; column++)
		{

			type = map[row][column];
			dest.x = column * 43 - points->getXCam();
			dest.y = row * 43 -points->getYCam();

			switch (type)
			{
			case 0:

				TextureManager::Draw(grass, src, dest);
				break;
			case 1:

				TextureManager::Draw(dirt, src, dest);
				break;
			case 2:

				TextureManager::Draw(wood, src, dest);
				break;
			case 3:

				TextureManager::Draw(stone, src, dest);
				break;
			case 4:

				TextureManager::Draw(darkStone, src, dest);
				break;
			case 5:

				TextureManager::Draw(fireOnStone, src, dest);
				break;
			case 6:
				TextureManager::Draw(otherGrass1, src, dest);
				break;

			default:

				break;
			}

		}

	}


}

